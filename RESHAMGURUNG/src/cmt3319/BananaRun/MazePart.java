package cmt3319.BananaRun;
/**
 * 
 * This class creates blocks on the screen and holds position
 *
 */
public class MazePart {
	private int x,y;
	
	public MazePart(int x, int y)
	{
		this.x = x;
		this.y = y;
	}
	public int getX(){
		return this.x;
	}
	public int getY(){
		return this.y;
	}

	

}
